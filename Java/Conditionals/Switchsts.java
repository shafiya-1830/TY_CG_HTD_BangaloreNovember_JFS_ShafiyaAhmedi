class Switchsts
{
public static void main(String[] args)
{
int a=10;
char c='M';
switch(a)
{
case 11:System.out.println("b");
break;
case 10:System.out.println("c");
break;
case 1:System.out.println(a);
break;

}
switch(c)
{
case 'A':System.out.println("Mishkat");
break;
case 'S':System.out.println("Steven");
break;
case 'M':System.out.println("Mahesh babu");
break;

}
}
}